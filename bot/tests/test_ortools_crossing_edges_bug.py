def segments_intersect(p1, p2, p3, p4):
    """
    Check if line segment p1-p2 intersects with p3-p4.
    Uses cross product method to detect intersection.
    """
    x1, y1 = p1
    x2, y2 = p2
    x3, y3 = p3
    x4, y4 = p4

    def ccw(A, B, C):
        return (C[1] - A[1]) * (B[0] - A[0]) > (B[1] - A[1]) * (C[0] - A[0])

    return ccw(p1, p3, p4) != ccw(p2, p3, p4) and ccw(p1, p2, p3) != ccw(p1, p2, p4)


def count_edge_crossings(tour_order, graph):
    """
    Count the number of edge crossings in a tour.
    Returns (crossing_count, crossing_details)
    """
    if len(tour_order) < 4:
        return 0, []

    waypoints = graph["waypoints"]
    edges = []

    # Build list of tour edges with coordinates
    for i in range(len(tour_order) - 1):
        wp1 = tour_order[i]
        wp2 = tour_order[i + 1]

        if wp1 not in waypoints or wp2 not in waypoints:
            continue

        x1, y1 = waypoints[wp1]["x"], waypoints[wp1]["y"]
        x2, y2 = waypoints[wp2]["x"], waypoints[wp2]["y"]
        edges.append(((x1, y1), (x2, y2), wp1, wp2))

    # Check each pair of edges for crossings
    crossings = []
    for i in range(len(edges)):
        for j in range(i + 2, len(edges)):  # Skip adjacent edges
            p1, p2, wp1a, wp1b = edges[i]
            p3, p4, wp2a, wp2b = edges[j]

            # Skip if edges share an endpoint
            if wp1b == wp2a or wp1a == wp2b:
                continue

            if segments_intersect(p1, p2, p3, p4):
                crossings.append({
                    "edge1_idx": i,
                    "edge2_idx": j,
                    "edge1": f"{wp1a} -> {wp1b}",
                    "edge2": f"{wp2a} -> {wp2b}"
                })

    return len(crossings), crossings


