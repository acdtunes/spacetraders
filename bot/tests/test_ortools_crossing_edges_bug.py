def test_ortools_with_longer_timeout_produces_better_solution(simple_grid_graph, ship_data):
    """
    Test that increasing timeout from 30s to 5 minutes improves solution quality.

    If 30s is producing suboptimal solutions with crossings, longer timeout should help.
    """
    config_30s = RoutingConfig()
    config_30s._config["solver"]["time_limit_ms"] = 30000

    config_300s = RoutingConfig()
    config_300s._config["solver"]["time_limit_ms"] = 300000  # 5 minutes

    with patch('spacetraders_bot.core.ortools_router.get_database') as mock_db:
        mock_db_instance = MagicMock()
        mock_db.return_value = mock_db_instance
        mock_db_instance.connection.return_value.__enter__.return_value = MagicMock()
        mock_db_instance.get_cached_tour.return_value = None

        waypoints = [
            "X1-TEST-A2", "X1-TEST-A3", "X1-TEST-B1", "X1-TEST-B2",
            "X1-TEST-B3", "X1-TEST-C1", "X1-TEST-C2", "X1-TEST-C3"
        ]

        # Test with 30s timeout
        tsp_30s = ORToolsTSP(simple_grid_graph, config_30s)
        tour_30s = tsp_30s.optimise_tour(
            waypoints=waypoints,
            start="X1-TEST-A1",
            ship_data=ship_data,
            return_to_start=True,
            use_cache=False,
            algorithm="ortools"
        )

        # Test with 5min timeout
        tsp_300s = ORToolsTSP(simple_grid_graph, config_300s)
        tour_300s = tsp_300s.optimise_tour(
            waypoints=waypoints,
            start="X1-TEST-A1",
            ship_data=ship_data,
            return_to_start=True,
            use_cache=False,
            algorithm="ortools"
        )

        assert tour_30s is not None
        assert tour_300s is not None

        tour_order_30s = [tour_30s["start"]] + tour_30s["stops"]
        tour_order_300s = [tour_300s["start"]] + tour_300s["stops"]

        crossings_30s, details_30s = count_edge_crossings(tour_order_30s, simple_grid_graph)
        crossings_300s, details_300s = count_edge_crossings(tour_order_300s, simple_grid_graph)

        print(f"\n30s timeout:")
        print(f"  Crossings: {crossings_30s}")
        print(f"  Distance: {tour_30s['total_distance']:.2f}")
        print(f"  Tour: {' -> '.join(tour_order_30s[:5])}...{tour_order_30s[-1]}")

        print(f"\n5min timeout:")
        print(f"  Crossings: {crossings_300s}")
        print(f"  Distance: {tour_300s['total_distance']:.2f}")
        print(f"  Tour: {' -> '.join(tour_order_300s[:5])}...{tour_order_300s[-1]}")

        # Longer timeout should produce equal or better solution
        assert tour_300s["total_distance"] <= tour_30s["total_distance"], \
            "Longer timeout should produce equal or better distance"
        assert crossings_300s <= crossings_30s, \
            "Longer timeout should produce equal or fewer crossings"


def test_ortools_initial_solution_strategy_impact(simple_grid_graph, ship_data):
    """
    Test different initial solution strategies to see if they affect crossing likelihood.

    PATH_CHEAPEST_ARC might produce better initial solutions than other strategies.
    """
    strategies = [
        "PATH_CHEAPEST_ARC",
        "PARALLEL_CHEAPEST_INSERTION",
        "LOCAL_CHEAPEST_INSERTION",
        "GLOBAL_CHEAPEST_ARC",
    ]

    results = {}

    for strategy in strategies:
        config = RoutingConfig()
        config._config["solver"]["first_solution_strategy"] = strategy
        config._config["solver"]["time_limit_ms"] = 30000

        with patch('spacetraders_bot.core.ortools_router.get_database') as mock_db:
            mock_db_instance = MagicMock()
            mock_db.return_value = mock_db_instance
            mock_db_instance.connection.return_value.__enter__.return_value = MagicMock()
            mock_db_instance.get_cached_tour.return_value = None

            tsp = ORToolsTSP(simple_grid_graph, config)

            waypoints = [
                "X1-TEST-A2", "X1-TEST-A3", "X1-TEST-B1", "X1-TEST-B2",
                "X1-TEST-B3", "X1-TEST-C1", "X1-TEST-C2", "X1-TEST-C3"
            ]

            tour = tsp.optimise_tour(
                waypoints=waypoints,
                start="X1-TEST-A1",
                ship_data=ship_data,
                return_to_start=True,
                use_cache=False,
                algorithm="ortools"
            )

            if tour:
                tour_order = [tour["start"]] + tour["stops"]
                crossings, _ = count_edge_crossings(tour_order, simple_grid_graph)
                results[strategy] = {
                    "crossings": crossings,
                    "distance": tour["total_distance"]
                }

    print(f"\nInitial solution strategy comparison:")
    for strategy, result in results.items():
        print(f"  {strategy}: {result['crossings']} crossings, distance {result['distance']:.2f}")

    # At least one strategy should produce a crossing-free solution
    min_crossings = min(r["crossings"] for r in results.values())
    assert min_crossings == 0, \
        f"At least one strategy should produce crossing-free tour, min was {min_crossings}"


