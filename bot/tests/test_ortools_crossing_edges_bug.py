def test_ortools_simple_grid_should_have_no_crossings(simple_grid_graph, ship_data, production_config):
    """
    Test that OR-Tools produces a tour with no edge crossings for a simple 3x3 grid.

    Expected optimal tour (spiral pattern):
    A1 -> A2 -> A3 -> B3 -> C3 -> C2 -> C1 -> B1 -> B2 -> A1

    OR-Tools with GUIDED_LOCAL_SEARCH should find this or a similar crossing-free solution.
    """
    with patch('spacetraders_bot.core.ortools_router.get_database') as mock_db:
        mock_db_instance = MagicMock()
        mock_db.return_value = mock_db_instance
        mock_db_instance.connection.return_value.__enter__.return_value = MagicMock()
        mock_db_instance.get_cached_tour.return_value = None  # Force optimization

        tsp = ORToolsTSP(simple_grid_graph, production_config)

        waypoints = [
            "X1-TEST-A2", "X1-TEST-A3", "X1-TEST-B1", "X1-TEST-B2",
            "X1-TEST-B3", "X1-TEST-C1", "X1-TEST-C2", "X1-TEST-C3"
        ]

        tour = tsp.optimise_tour(
            waypoints=waypoints,
            start="X1-TEST-A1",
            ship_data=ship_data,
            return_to_start=True,
            use_cache=False,
            algorithm="ortools"
        )

        assert tour is not None, "Tour optimization should succeed"

        # Extract tour order (start + stops + return)
        tour_order = [tour["start"]] + tour["stops"]

        print(f"\nTour order: {' -> '.join(tour_order)}")
        print(f"Total distance: {tour['total_distance']:.2f}")
        print(f"Total time: {tour['total_time']}s")

        # Count edge crossings
        crossing_count, crossing_details = count_edge_crossings(tour_order, simple_grid_graph)

        if crossing_count > 0:
            print(f"\n❌ Found {crossing_count} crossing edges:")
            for crossing in crossing_details:
                print(f"  - {crossing['edge1']} crosses {crossing['edge2']}")
        else:
            print(f"\n✅ No crossing edges found")

        assert crossing_count == 0, \
            f"Tour should have no edge crossings, but found {crossing_count}: {crossing_details}"


