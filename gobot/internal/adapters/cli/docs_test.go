// TestDocsSubcommandDefaultDirs pins the default --dir values to what the
// Makefile targets (man, cli-docs) assume, so the two can't silently drift.
func TestDocsSubcommandDefaultDirs(t *testing.T) {
	manCmd := newDocsManCommand()
	flag := manCmd.Flags().Lookup("dir")
	require.NotNil(t, flag)
	require.Equal(t, "build/man/man1", flag.DefValue)

	markdownCmd := newDocsMarkdownCommand()
	flag = markdownCmd.Flags().Lookup("dir")
	require.NotNil(t, flag)
	require.Equal(t, "docs/cli", flag.DefValue)
}

// TestDocsMarkdownCommandOmitsAutoGenTag guards against a real near-miss:
// cobra's doc generators stamp an "Autogenerated by spf13/cobra on <date>"
// footer into every file by default. docs/cli/*.md is committed for
// reviewable diffs (see Makefile), so an unsuppressed date footer would dirty
// all 120 files on every `make cli-docs` regen regardless of whether any
// command actually changed — defeating the entire point of committing them.
// `docs man`/`docs markdown` set root.DisableAutoGenTag = true before
// generating; this asserts that actually holds for the real RunE path (not
// just the bare doc.GenMarkdownTree calls in the smoke tests above, which
// intentionally don't exercise that flag).
func TestDocsMarkdownCommandOmitsAutoGenTag(t *testing.T) {
	dir := t.TempDir()
	root := NewRootCommand()

	target, _, err := root.Find([]string{"docs", "markdown"})
	require.NoError(t, err)
	require.NoError(t, target.Flags().Set("dir", dir))
	require.NoError(t, target.RunE(target, nil))

	content, err := os.ReadFile(filepath.Join(dir, "spacetraders_ship_list.md"))
	require.NoError(t, err)
	require.NotContains(t, string(content), "Auto generated by spf13/cobra",
		"generated markdown must not carry a date-stamped footer — it would dirty every committed file on every regen")
}

