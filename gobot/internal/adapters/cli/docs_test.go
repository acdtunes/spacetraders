// TestGenManTreeProducesFiles is a smoke test, not a golden-file test: it
// asserts GenManTree runs over the real root command without error and
// produces output, not the exact rendered content (which would make every
// command's Short/Long text a change here).
func TestGenManTreeProducesFiles(t *testing.T) {
	dir := t.TempDir()

	header := &doc.GenManHeader{Title: "SPACETRADERS", Section: "1"}
	err := doc.GenManTree(NewRootCommand(), header, dir)
	require.NoError(t, err)

	entries, err := os.ReadDir(dir)
	require.NoError(t, err)
	require.NotEmpty(t, entries, "GenManTree must produce at least one man page")

	require.FileExists(t, filepath.Join(dir, "spacetraders-ship-list.1"),
		"a known leaf command must have a generated man page")
}

// TestGenManTreeHidesDocsCommand confirms the hidden `docs` command itself
// (Hidden: true in NewDocsCommand) does not appear in its own output — it
// documents the tree, so it must not pollute the tree it documents.
func TestGenManTreeHidesDocsCommand(t *testing.T) {
	dir := t.TempDir()

	err := doc.GenManTree(NewRootCommand(), &doc.GenManHeader{Title: "SPACETRADERS", Section: "1"}, dir)
	require.NoError(t, err)

	require.NoFileExists(t, filepath.Join(dir, "spacetraders-docs.1"))
	require.NoFileExists(t, filepath.Join(dir, "spacetraders-docs-man.1"))
}

