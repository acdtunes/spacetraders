// TestDocsCommandHiddenFromHelp confirms `docs` does not clutter
// `spacetraders --help` (the surface it exists to document) while remaining
// directly invokable — same contract as any other hidden cobra command.
func TestDocsCommandHiddenFromHelp(t *testing.T) {
	root := NewRootCommand()

	var found *bool
	for _, c := range root.Commands() {
		if c.Name() == "docs" {
			v := c.Hidden
			found = &v
		}
	}

	require.NotNil(t, found, "docs command must be registered on root")
	require.True(t, *found, "docs command must be Hidden so it doesn't clutter --help")
}

// TestDocsManCommandRunEWritesRealTree exercises the wired `docs man` RunE
// closure through the real root (--dir flag, MkdirAll, cmd.Root()
// resolution), not just the underlying doc.GenManTree call.
